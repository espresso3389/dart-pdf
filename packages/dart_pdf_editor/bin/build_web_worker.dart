// Builds the dedicated Web Worker that backs dart_pdf_editor's off-thread page
// renderer, so consuming apps don't have to hand-write a worker entry or
// remember the exact `dart compile js` invocation.
//
// Run it from your app's root (where pubspec.yaml lives):
//
//   dart run dart_pdf_editor:build_web_worker
//
// It generates a tiny worker entry under .dart_tool/ and compiles it to
// `web/pdf_render_worker.dart.js`, which `flutter build web` then serves next
// to index.html. The app opts in by setting (on web):
//
//   pdfRenderWorkerScriptUrl = 'pdf_render_worker.dart.js';
//
// You can commit the produced `web/pdf_render_worker.dart.js` and only re-run
// this when you upgrade dart_pdf_editor — it does NOT have to run every build.
// See doc/render_worker_web.md in the repo for the full picture.
import 'dart:io';

const _entryName = 'pdf_render_worker.dart';
const _defaultOut = 'web/pdf_render_worker.dart.js';

const _entrySource = '''
// GENERATED by `dart run dart_pdf_editor:build_web_worker` — do not edit.
// The dedicated Web Worker entry for the off-thread PDF page renderer.
import 'package:dart_pdf_editor/render_worker_web.dart';

void main() => runPdfRenderWorker();
''';

Future<int> main(List<String> args) async {
  if (args.contains('-h') || args.contains('--help')) {
    stdout.writeln(_usage);
    return 0;
  }

  var out = _defaultOut;
  var optimize = true;
  var sourceMaps = false;
  for (var i = 0; i < args.length; i++) {
    final arg = args[i];
    if (arg == '--out' || arg == '-o') {
      if (i + 1 >= args.length) {
        stderr.writeln('error: $arg needs a path');
        return 64;
      }
      out = args[++i];
    } else if (arg.startsWith('--out=')) {
      out = arg.substring('--out='.length);
    } else if (arg == '--no-optimize') {
      optimize = false;
    } else if (arg == '--source-maps') {
      sourceMaps = true;
    } else {
      stderr.writeln('error: unknown argument "$arg"\n');
      stderr.writeln(_usage);
      return 64;
    }
  }

  // Must run from an app root that depends on dart_pdf_editor.
  if (!File('pubspec.yaml').existsSync()) {
    stderr.writeln(
      'error: run this from your app root (where pubspec.yaml lives).',
    );
    return 64;
  }
  // The package config can live above cwd (pub workspaces keep one at the
  // workspace root), so walk up to find it — the same search Dart itself does.
  final packageConfig = _findPackageConfig(Directory.current);
  if (packageConfig == null) {
    stderr.writeln(
      'error: no .dart_tool/package_config.json found — run '
      '`flutter pub get` first.',
    );
    return 64;
  }
  if (!packageConfig.readAsStringSync().contains('"dart_pdf_editor"')) {
    stderr.writeln(
      'error: this project does not depend on dart_pdf_editor — add it to '
      'pubspec.yaml\n       and run `flutter pub get` first.',
    );
    return 64;
  }

  // Generate the worker entry beside the package config's .dart_tool, so it
  // never clutters the app's own web/ sources — only the compiled .js lands in
  // web/.
  final entryDir = Directory('${packageConfig.parent.path}/dart_pdf_editor')
    ..createSync(recursive: true);
  final entry = File('${entryDir.path}/$_entryName')
    ..writeAsStringSync(_entrySource);

  Directory(_dirOf(out)).createSync(recursive: true);

  final cmd = [
    'compile',
    'js',
    if (optimize) '-O2',
    if (!sourceMaps) '--no-source-maps',
    '--packages=${packageConfig.path}',
    '-o',
    out,
    entry.path,
  ];

  stdout.writeln('==> dart ${cmd.join(' ')}');
  final result = await Process.start(
    'dart',
    cmd,
    mode: ProcessStartMode.inheritStdio,
  );
  final code = await result.exitCode;
  if (code != 0) {
    stderr.writeln('error: worker compile failed (exit $code).');
    return code;
  }

  stdout.writeln('''
==> Built $out

Add this once to your app's main() (web only):

    import 'package:dart_pdf_editor/dart_pdf_editor.dart';
    import 'package:flutter/foundation.dart';

    void main() {
      if (kIsWeb) {
        pdfRenderWorkerScriptUrl = '${_basename(out)}';
      }
      runApp(...);
    }

Then `flutter build web` serves the worker. You can commit $out and only
re-run this tool when you upgrade dart_pdf_editor.''');
  return 0;
}

/// Walks up from [start] looking for `.dart_tool/package_config.json`, the way
/// the Dart toolchain resolves packages — so this works from a plain app root
/// (config in the app) and from inside a pub workspace (config at the root).
File? _findPackageConfig(Directory start) {
  for (var dir = start; ; dir = dir.parent) {
    final candidate = File('${dir.path}/.dart_tool/package_config.json');
    if (candidate.existsSync()) return candidate;
    if (dir.path == dir.parent.path) return null; // reached the filesystem root
  }
}

String _dirOf(String path) {
  final i = path.lastIndexOf('/');
  return i <= 0 ? '.' : path.substring(0, i);
}

String _basename(String path) {
  final i = path.lastIndexOf('/');
  return i < 0 ? path : path.substring(i + 1);
}

const _usage = '''
Builds the dart_pdf_editor Web Worker render backend.

Usage: dart run dart_pdf_editor:build_web_worker [options]

Options:
  -o, --out <path>   Output JS path (default: $_defaultOut).
      --no-optimize  Skip -O2 (faster compile, larger output).
      --source-maps  Emit a .js.map alongside the worker.
  -h, --help         Show this help.

Run from your app root. Commit the output and re-run only when you upgrade
dart_pdf_editor; it does not need to run on every build.''';
