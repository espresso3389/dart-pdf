void main() {
  test('lossless gray (5/3) decodes bit-perfectly', () {
    final image = JpxDecoder.decode(Uint8List.fromList(_grayJ2k))!;
    expect(image.width, 16);
    expect(image.height, 16);
    expect(image.components, 1);
    expect(image.samples, _grayExpected);
  });

  test('JP2 containers unwrap to the same codestream', () {
    final image = JpxDecoder.decode(Uint8List.fromList(_grayJp2))!;
    expect(image.samples, _grayExpected);
  });

  test('lossless RGB (RCT + 5/3) decodes bit-perfectly', () {
    final image = JpxDecoder.decode(Uint8List.fromList(_rgbJ2k))!;
    expect(image.components, 3);
    expect(image.samples, _rgbExpected);
  });

  test('lossy RGB (ICT + 9/7, 4:1) matches the reference decoder', () {
    final image = JpxDecoder.decode(Uint8List.fromList(_lossyJ2k))!;
    expect(image.samples.length, _lossyReference.length);
    for (var i = 0; i < _lossyReference.length; i++) {
      expect((image.samples[i] - _lossyReference[i]).abs(),
          lessThanOrEqualTo(3),
          reason: 'sample $i');
    }
  });

  test('reset-probabilities code-block style decodes bit-perfectly', () {
    final image = JpxDecoder.decode(Uint8List.fromList(_resetProbJ2k))!;
    expect(image.width, 16);
    expect(image.height, 16);
    expect(image.components, 1);
    expect(image.samples, _resetProbExpected);
  });

  test('garbage decodes to null, not an exception', () {
    expect(
        JpxDecoder.decode(
            Uint8List.fromList('definitely not jpeg 2000'.codeUnits)),
        isNull);
  });

  test('a multi-tile-part codestream decodes without desyncing', () {
    // issue3371.pdf: six tile-parts. The SOT Psot offset bug used to fold the
    // later tile-parts' markers into the packet bitstream, exploding the
    // chroma into noise (every pixel max-min channel ~255). Correctly the
    // tile is near-grayscale text on a white ground.
    final image = JpxDecoder.decode(Uint8List.fromList(_issue3371Im3))!;
    expect(image.width, 58);
    expect(image.height, 52);
    expect(image.components, 3);
    final s = image.samples;
    int chroma(int i) {
      final r = s[i * 3], g = s[i * 3 + 1], b = s[i * 3 + 2];
      return [r, g, b].reduce(math.max) - [r, g, b].reduce(math.min);
    }

    var maxChroma = 0;
    for (var i = 0; i < image.width * image.height; i++) {
      maxChroma = math.max(maxChroma, chroma(i));
    }
    // a faithful decode stays near-neutral; the desync bug hit 255
    expect(maxChroma, lessThan(80));
    // white corner, dark glyph ink near the centre (text is present)
    expect(s[0], greaterThan(240));
    final mid = (image.height ~/ 2) * image.width + image.width ~/ 2;
    expect(s[mid * 3], lessThan(120));
  });
}
